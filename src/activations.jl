function Пғ(x)
    t = exp(-abs(x))
    ifelse(x вүҘ 0, inv(1 + t), t / (1 + t))
end

# Complex inputs (e.g. complex-valued networks): Пғ is holomorphic, so we compute the
# same function via the overflow-safe rearrangement. `1/(1+exp(-z))` and
# `exp(z)/(1+exp(z))` are equal everywhere; we branch on `real(z)` to pick the one
# whose `exp` cannot overflow (overflow depends only on the real part).
function Пғ(z::Complex)
    real(z) вүҘ 0 ? inv(1 + exp(-z)) : (t = exp(z); t / (1 + t))
end

const sigmoid = Пғ

