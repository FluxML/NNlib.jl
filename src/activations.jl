function gelu_tanh(x)
    Оұ = oftf(x, 0.044715)
    О» = oftf(x, gelu_О»)
    x/2 * (1 + tanh_fast(О» * (x + Оұ * x^3)))
end

const gelu_О» = вҲҡ(2 / ПҖ)
const gelu_2О» = вҲҡ(8 / ПҖ)

function deriv_gelu_tanh(x)
    Оұ = oftf(x, 0.044715)
    Оұ2 = oftf(x, 0.08943)
    О» = oftf(x, gelu_О»)
    x2 = x * x
    t = muladd(x2, Оұ, one(x))
    z = О» * x * t
    О© = tanh_fast(z)
    sech2 = 1 - О©^2
    (1 + О©)/2 + x * О» * muladd(x2, Оұ2, t) * sech2 / 2
end

"""
